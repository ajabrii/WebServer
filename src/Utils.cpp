#include "../includes/Utils.hpp"
#include <ctime>

//! add this function to header
std::string clean_line(std::string line)
{
    line.erase(0, line.find_first_not_of(" \t\n\r"));
    line.erase(line.find_last_not_of(" \t\n\r") + 1);

    if (!line.empty() && line.back() == ';')
    {
        line.pop_back();
    }
    return line;
}